package practice.mohan;
//Highest no in Array
public class ProgramNo3 {public static void main(String[] args) {
    int a[]={23,43,99,33,101};
    int max = a[0];
    for (int i = 1; i <a.length; i++) {
        if (a[i]>max){
            max=a[i];
        }






    }
    System.out.print("Largest "+max);

}

}
